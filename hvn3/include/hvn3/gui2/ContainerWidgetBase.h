#pragma once
#include "hvn3/gui2/WidgetBase.h"

namespace hvn3 {
	namespace Gui {

		class ContainerWidgetBase : public virtual WidgetBase {

		public:
			ContainerWidgetBase() :
				_manager(this) {
			}

			bool HasChildren() override {
				return true;
			}
			WidgetManager& GetChildren() override {
				return _manager;
			}

		private:
			WidgetManager _manager;

		};

	}
}