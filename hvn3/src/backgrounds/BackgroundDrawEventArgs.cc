#include "backgrounds/BackgroundDrawEventArgs.h"
#include "views/View.h"

namespace hvn3 {

	BackgroundDrawEventArgs::BackgroundDrawEventArgs(Graphics::Graphics& graphics, const SizeI& room_size, const View* current_view, bool draw_foregrounds) :
		DrawEventArgs(graphics),
		_room_size(room_size),
		_current_view(current_view) {

		_draw_foregrounds = draw_foregrounds;

	}
	const View* BackgroundDrawEventArgs::CurrentView() const {

		return _current_view;

	}
	const SizeI& BackgroundDrawEventArgs::RoomSize() const {

		return _room_size;

	}
	bool BackgroundDrawEventArgs::DrawForegrounds() const {

		return _draw_foregrounds;

	}

}